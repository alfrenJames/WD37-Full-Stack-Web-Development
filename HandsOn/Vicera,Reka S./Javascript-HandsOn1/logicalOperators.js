num1=4;
if(num1>1 && num1<5){
    console.log("&& result: true")
}
if(num1>1 || num1<5){
    console.log("|| result: true")
}
if(num1!=0){
    console.log("! result: true")
}
console.log("Ternary result:",num1==4?true:false);